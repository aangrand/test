#include <unistd.h>

void ft_is_negative(int n) 
{
	int N;
	int P;

	N = 78;
	P = 80;
	if (n < 0)
	{
		write(1, &N, 1);	
	}
	else 
	{
		write(1, &P, 1);
	}
}

int		main(void)
{
	ft_is_negative(-50);
	return (0);
}
