int		ft_sqrt(int nb)
{
	int i;

	i = 0;
	if (nb < 46341)
	{
		while((i * i) < nb)
		{
			i++;
			if (i * i > nb)
				return(0); 
		}
		return (i);
	}
}

int		main()
{
	printf("%d", ft_sqrt(100));
}
